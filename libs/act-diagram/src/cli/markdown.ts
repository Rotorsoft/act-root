/**
 * Render the full contract index as a Markdown registry. One section
 * per kind, one subsection per entity. Used by `act --markdown` for
 * pipe-friendly output that diffs cleanly between branches.
 *
 * Output is plain ASCII (no ANSI escapes) regardless of `picocolors`
 * settings — readers expect Markdown to be paste-able into a PR body
 * or a static doc file.
 */
import type { DomainModel } from "../client/types/index.js";
import {
  type ContractIndex,
  event_status,
  list_by_kind,
} from "./contract-index.js";

const loc = (file?: string, line?: number): string => {
  if (!file) return "";
  return line ? `${file}:${line}` : file;
};

const find_actions_emitting = (
  model: DomainModel,
  event_name: string
): Array<{ state: string; action: string; file?: string; line?: number }> => {
  const out: Array<{
    state: string;
    action: string;
    file?: string;
    line?: number;
  }> = [];
  for (const st of model.states) {
    for (const act of st.actions) {
      if (act.emits.includes(event_name)) {
        out.push({
          state: st.name,
          action: act.name,
          file: st.file,
          line: act.line,
        });
      }
    }
  }
  return out;
};

const find_reactions_for = (
  model: DomainModel,
  event_name: string
): Array<{
  slice?: string;
  handler: string;
  dispatches: string[];
  file?: string;
  line?: number;
}> => {
  const out: Array<{
    slice?: string;
    handler: string;
    dispatches: string[];
    file?: string;
    line?: number;
  }> = [];
  for (const sl of model.slices) {
    for (const r of sl.reactions) {
      if (r.event === event_name) {
        out.push({
          slice: sl.name,
          handler: r.handler_name,
          dispatches: r.dispatches,
          file: r.file ?? sl.file,
          line: r.line,
        });
      }
    }
  }
  for (const r of model.reactions) {
    if (r.event === event_name) {
      out.push({
        handler: r.handler_name,
        dispatches: r.dispatches,
        file: r.file,
        line: r.line,
      });
    }
  }
  return out;
};

const find_projections_for = (
  model: DomainModel,
  event_name: string
): Array<{ name: string; file?: string }> =>
  model.projections
    .filter((p) => p.handles.includes(event_name))
    .map((p) => ({ name: p.name, file: p.file }));

const file_suffix = (file?: string, line?: number): string => {
  const l = loc(file, line);
  return l ? `  *(${l})*` : "";
};

/** Render a captured Zod expression. Single-line goes inline with
 *  backticks; multi-line gets a fenced code block so newlines survive. */
const render_schema = (s: string | undefined): string => {
  if (!s) return "_(not captured)_";
  if (!s.includes("\n")) return `\`${s}\``;
  const indented = s
    .split("\n")
    .map((l) => `  ${l}`)
    .join("\n");
  return `\n\n  \`\`\`ts\n${indented}\n  \`\`\``;
};

export function format_markdown(idx: ContractIndex): string {
  const m = idx.model;
  const lines: string[] = [];

  lines.push("# Act Contracts Registry");
  lines.push("");
  lines.push(
    `_Generated by \`act --markdown\`. ${m.states.length} states, ${m.slices.length} slices, ${m.projections.length} projections, ${idx.all_event_names.size} events._`
  );
  lines.push("");

  // ── Events ──────────────────────────────────────────────────────────
  lines.push("## Events");
  lines.push("");
  const events = list_by_kind(idx, "event");
  if (events.length === 0) lines.push("_(none)_\n");
  for (const e of events) {
    let schema: string | undefined;
    let owning_state: string | undefined;
    for (const st of m.states) {
      const ev = st.events.find((x) => x.name === e.name);
      if (ev) {
        schema = ev.schema;
        owning_state = st.name;
        break;
      }
    }
    const status = event_status(e.name, idx.all_event_names);
    const status_label =
      status.status === "active"
        ? "active"
        : `deprecated — superseded by **${status.superseded_by}**`;
    lines.push(`### \`${e.name}\``);
    // `e.file` always carries the state's source file via build_contract_index.
    if (e.file) lines.push(`- **defined in:** \`${loc(e.file, e.line)}\``);
    if (owning_state && owning_state !== e.name)
      lines.push(`- **on state:** \`${owning_state}\``);
    lines.push(`- **schema:** ${render_schema(schema)}`);
    lines.push(`- **status:** ${status_label}`);

    const producers = find_actions_emitting(m, e.name);
    if (producers.length > 0) {
      lines.push("- **producers:**");
      for (const p of producers)
        lines.push(
          `  - \`${p.action}\` (state \`${p.state}\`)${file_suffix(p.file, p.line)}`
        );
    } else {
      lines.push("- **producers:** _(none)_");
    }
    const reactions = find_reactions_for(m, e.name);
    const projections = find_projections_for(m, e.name);
    if (reactions.length === 0 && projections.length === 0) {
      lines.push("- **consumers:** _(none)_");
    } else {
      lines.push("- **consumers:**");
      for (const r of reactions) {
        const slice = r.slice ? `\`${r.slice}\`::` : "";
        const triggers =
          r.dispatches.length > 0
            ? ` → triggers ${r.dispatches.map((d) => `\`${d}\``).join(", ")}`
            : "";
        lines.push(
          `  - reaction ${slice}\`${r.handler}\`${triggers}${file_suffix(r.file, r.line)}`
        );
      }
      for (const p of projections)
        lines.push(`  - projection \`${p.name}\`${file_suffix(p.file)}`);
    }
    lines.push("");
  }

  // ── Actions ─────────────────────────────────────────────────────────
  lines.push("## Actions");
  lines.push("");
  const actions = list_by_kind(idx, "action");
  if (actions.length === 0) lines.push("_(none)_\n");
  for (const a of actions) {
    let action:
      | { name: string; emits: string[]; invariants: string[]; line?: number }
      | undefined;
    let state_file: string | undefined;
    for (const st of m.states) {
      if (a.qualifier && st.name !== a.qualifier) continue;
      const found = st.actions.find((x) => x.name === a.name);
      if (found) {
        action = found;
        state_file = st.file;
        break;
      }
    }
    lines.push(`### \`${a.name}\``);
    if (a.qualifier) lines.push(`- **on state:** \`${a.qualifier}\``);
    if (state_file)
      lines.push(
        `- **defined in:** \`${loc(state_file, action?.line ?? a.line)}\``
      );
    if (action) {
      if (action.invariants.length > 0) {
        lines.push("- **invariants:**");
        for (const inv of action.invariants) lines.push(`  - ${inv}`);
      }
      if (action.emits.length > 0) {
        lines.push("- **emits:**");
        for (const ev of action.emits) lines.push(`  - \`${ev}\``);
      } else {
        lines.push("- **emits:** _(none)_");
      }
    }
    lines.push("");
  }

  // ── States ──────────────────────────────────────────────────────────
  lines.push("## States");
  lines.push("");
  const states = list_by_kind(idx, "state");
  if (states.length === 0) lines.push("_(none)_\n");
  for (const s of states) {
    const st = m.states.find((x) => x.name === s.name);
    lines.push(`### \`${s.name}\``);
    if (s.file) lines.push(`- **defined in:** \`${loc(s.file, s.line)}\``);
    if (st) {
      if (st.actions.length > 0)
        lines.push(
          `- **actions:** ${st.actions.map((x) => `\`${x.name}\``).join(", ")}`
        );
      if (st.events.length > 0)
        lines.push(
          `- **events:** ${st.events.map((x) => `\`${x.name}\``).join(", ")}`
        );
    }
    lines.push("");
  }

  // ── Slices ──────────────────────────────────────────────────────────
  lines.push("## Slices");
  lines.push("");
  const slices = list_by_kind(idx, "slice");
  if (slices.length === 0) lines.push("_(none)_\n");
  for (const s of slices) {
    const sl = m.slices.find((x) => x.name === s.name);
    lines.push(`### \`${s.name}\``);
    if (s.file) lines.push(`- **defined in:** \`${loc(s.file, s.line)}\``);
    if (sl) {
      if (sl.error) lines.push(`- **error:** ${sl.error}`);
      if (sl.states.length > 0)
        lines.push(
          `- **states:** ${sl.states.map((x) => `\`${x}\``).join(", ")}`
        );
      if (sl.projections.length > 0)
        lines.push(
          `- **projections:** ${sl.projections.map((x) => `\`${x}\``).join(", ")}`
        );
      if (sl.reactions.length > 0) {
        lines.push("- **reactions:**");
        for (const r of sl.reactions) {
          const triggers =
            r.dispatches.length > 0
              ? ` → ${r.dispatches.map((d) => `\`${d}\``).join(", ")}`
              : "";
          lines.push(
            `  - \`${r.event}\` → \`${r.handler_name}\`${triggers}${file_suffix(r.file, r.line)}`
          );
        }
      }
    }
    lines.push("");
  }

  // ── Projections ─────────────────────────────────────────────────────
  lines.push("## Projections");
  lines.push("");
  const projections = list_by_kind(idx, "projection");
  if (projections.length === 0) lines.push("_(none)_\n");
  for (const p of projections) {
    const pr = m.projections.find((x) => x.name === p.name);
    lines.push(`### \`${p.name}\``);
    if (pr?.file) lines.push(`- **defined in:** \`${loc(pr.file, pr.line)}\``);
    if (pr && pr.handles.length > 0)
      lines.push(
        `- **handles:** ${pr.handles.map((x) => `\`${x}\``).join(", ")}`
      );
    lines.push("");
  }

  // ── Reactions ───────────────────────────────────────────────────────
  lines.push("## Reactions");
  lines.push("");
  const reactions = list_by_kind(idx, "reaction");
  if (reactions.length === 0) lines.push("_(none)_\n");
  for (const r of reactions) {
    const [slice_name, event_name] = (r.qualifier ?? "::").split("::");
    lines.push(`### \`${r.name}\``);
    if (slice_name && slice_name !== "*")
      lines.push(`- **in slice:** \`${slice_name}\``);
    if (event_name) lines.push(`- **on event:** \`${event_name}\``);
    if (r.file) lines.push(`- **defined in:** \`${loc(r.file, r.line)}\``);
    const slice_reaction = m.slices
      .find((x) => x.name === slice_name)
      ?.reactions.find(
        (rr) => rr.handler_name === r.name && rr.event === event_name
      );
    const orch_reaction = m.reactions.find(
      (rr) => rr.handler_name === r.name && rr.event === event_name
    );
    const inner = slice_reaction ?? orch_reaction;
    if (inner && inner.dispatches.length > 0) {
      lines.push(
        `- **triggers:** ${inner.dispatches.map((d) => `\`${d}\``).join(", ")}`
      );
    }
    lines.push("");
  }

  return lines.join("\n");
}
